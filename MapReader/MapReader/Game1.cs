        public void readFromFile(String path)
        {
            int x = 0;
            int y = 0;
            List<Body> bodies = new List<Body>();
            World world = new World(new Vector2(0, 300));

            string[] readText = System.IO.File.ReadAllLines(path);
            foreach (string s in readText)
            {
                x = 0;
                while (x < s.Length)
                {
                    if (s[x] != '0')
                    {
                        Body bodyTemp = BodyFactory.CreateRectangle(world, 32, 32, 1f);
                        bodyTemp.BodyType = BodyType.Static;
                        bodyTemp.Position = new Vector2(x * 32, y * 32);

                        bodies.Add(bodyTemp);
                    }
                    ++x;
                }
                ++y;
            }
        }
